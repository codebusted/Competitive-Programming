// linked list implementation
#include <iostream>
#include <cstddef>
using namespace std;	
class Node
{
    public:
        int data;
        Node *next;
        Node(int d){
            data=d;
            next=NULL;
        }
};
class Solution{
    public:

      Node* insert(Node *head,int data)
      {
          //Complete this method
          Node* newnode = new Node(data);
          Node* temp;
          if(head == NULL) {
              head = newnode;
              temp = newnode;
          }
          else {
              // head->next = last;
              temp->next = newnode;
              cout << temp->next << endl;
              temp = newnode;
          }
          return head;
      }

      void display(Node *head)
      {
          Node *start=head;
          while(start)
          {
              cout<<start->data<<" ";
              start=start->next;
          }
          cout << endl;
      }
};
